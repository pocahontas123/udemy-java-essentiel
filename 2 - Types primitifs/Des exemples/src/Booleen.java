
public class Booleen {

    public static void main(String[] args) {
        //Les booléens (boolean)
        boolean boolTrue = true;
        boolean boolFalse = false;

        System.out.println(boolTrue);//true
        System.out.println(boolFalse);//false

        //petite avant première sur les expressions (vraie ou fausse)
        System.out.println(5 > 1);//true
        System.out.println(5 >= 1);//true
        System.out.println(5 < 1);//false
        System.out.println(5 <= 1);//false
        System.out.println(1 <= 1);//true
        System.out.println(5 == 5);//true
        System.out.println("1234".length() == 4);//true
        System.out.println("1234".length() == 5);//false
    }
}

